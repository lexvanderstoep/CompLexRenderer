package Model;

import java.awt.*;

public class Sphere {
    double x, y, z;
    double radius;
    Color color;

    public Sphere(double x, double y, double z, double radius, Color color) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.radius = radius;
        this.color = color;
    }
}
